package ClassesAll;

public class Position {
    private int X;
    private int Y;
    public Position(int x,int y) {
        setX(x);
        setY(y);
    }
    public int getY() {
        return Y;
    }
    public void setY(int y) {
        Y = y;
    }
    public int getX() {
        return X;
    }
    public void setX(int x) {
        X = x;
    }


}
